"use strict";
/* Scans public/art and writes src/art-manifest.js, a map from glyph id to
   painted art path. The loader in src/art.js renders the painted file when
   an id is present and falls back to the inline SVG symbol when it is not,
   so art can land one batch at a time without breaking the build.

   Filename conventions (the id is the filename without its extension):
     public/art/items/dagger.png     -> g-dagger
     public/art/monsters/debt.png    -> m-debt   (glyph suffix, so the Debt
                                                  Collector's file is debt.png)
     public/art/portraits/p0.png     -> p-0
   Both .png and .webp are accepted (the asset diet ships WebP; ingest still
   drops PNG). When both exist for one id the PNG wins, because a fresh
   ingest drop is newer art than the dieted WebP beside it; running
   scripts/art-diet.js again folds the new drop into the diet.
   frames, board, and bg are referenced from CSS when that art lands; they do
   not go through the glyph manifest.

   Music (Suno tracks) lives in public/music and is listed under
   music-{name}, e.g. public/music/market.mp3 -> music-market. */
import {readdirSync, readFileSync, writeFileSync, existsSync} from 'node:fs';
import {fileURLToPath} from 'node:url';
import {dirname, join, resolve} from 'node:path';

const root = dirname(dirname(fileURLToPath(import.meta.url)));

export function scanArt(rootDir) {
  const map = {};
  const dirs = [
    ['items', function (n) { return 'g-' + n; }],
    ['monsters', function (n) { return 'm-' + n; }],
    ['portraits', function (n) { return n.replace(/^p(\d+)$/, 'p-$1'); }],
    ['ui', function (n) { return 'g-' + n; }],
    ['frames', function (n) { return n.replace(/_/g, '-'); }],
    ['board', function (n) { return n.replace(/_/g, '-'); }],
    ['bg', function (n) { return n.replace(/_/g, '-'); }]
  ];
  for (const [dir, toId] of dirs) {
    const p = join(rootDir, 'public', 'art', dir);
    if (!existsSync(p)) continue;
    for (const f of readdirSync(p).sort()) {
      const m = f.match(/^(.+)\.(png|webp)$/);
      if (!m) continue;
      const id = toId(m[1]);
      /* .png beats .webp for one id: a fresh ingest drop outranks the diet */
      if (map[id] && map[id].endsWith('.png') && m[2] === 'webp') continue;
      map[id] = 'art/' + dir + '/' + f;
    }
  }
  const mus = join(rootDir, 'public', 'music');
  if (existsSync(mus)) {
    for (const f of readdirSync(mus).sort()) {
      const m = f.match(/^(.+)\.(mp3|wav|m4a)$/);
      if (m) map['music-' + m[1]] = 'music/' + f;
    }
  }
  return map;
}

export function manifestSource(map) {
  const lines = Object.keys(map).sort().map(function (id) {
    return ' ' + JSON.stringify(id) + ':' + JSON.stringify(map[id]);
  });
  return '/* Generated by scripts/make-art-manifest.js. Do not edit by hand. */\n'
    + 'export const ART={' + (lines.length ? '\n' + lines.join(',\n') + '\n' : '') + '};\n';
}

export function writeManifest(rootDir) {
  const out = join(rootDir, 'src', 'art-manifest.js');
  const map = scanArt(rootDir);
  const src = manifestSource(map);
  const prev = existsSync(out) ? readFileSync(out, 'utf8') : '';
  if (src !== prev) writeFileSync(out, src);
  return {count: Object.keys(map).length, changed: src !== prev};
}

const isMain = process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) {
  const r = writeManifest(root);
  console.log(r.changed ? 'art manifest written: ' + r.count + ' painted assets' : 'art manifest unchanged');
}
